package com.rokzin.myschool.core;

public class CustomMenuItem {

	private int image;
	private String desc;
	
	public CustomMenuItem(int image, String desc) {
		super();
		this.image = image;
		this.desc = desc;
	}

	public int getImage() {
		return image;
	}

	public String getDesc() {
		return desc;
	}
	
}
